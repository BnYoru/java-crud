    public void selectData(){
        String kolom[] = {"idmobil","namamobil","merek","jenis","tahun","platnomor"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM datamobil";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()){
                String idmobil = rs.getString(1);
                String namamobil = rs.getString(2);
                String merek = rs.getString(3);
                String jenis = rs.getString(4);
                String tahun = rs.getString(5);
                String platnomor = rs.getString(6);
                String data[] = {idmobil,namamobil,merek,jenis,tahun,platnomor};
                dtm.addRow(data);
            }
        }catch (SQLException ex){
                Logger.getLogger(DataMobil.class.getName()).log(Level.SEVERE, null, ex);
        }
        tabel.setModel(dtm);
    }
    
