    private long hitungLamaSewa() {
        Date tanggalPengambilan = tanggalpengambilan.getDate();
        Date tanggalPengembalian = tanggalpengembalian.getDate();

        if (tanggalPengambilan != null && tanggalPengembalian != null) {
            long diffInMillies = Math.abs(tanggalPengembalian.getTime() - tanggalPengambilan.getTime());
            long diffInDays = diffInMillies / (24 * 60 * 60 * 1000);
            return diffInDays;
        }
        return 0;
    }

