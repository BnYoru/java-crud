    private void loadUserData(String idtransaksi) {
        String SQL = "SELECT * FROM datatransaksi WHERE idtransaksi = '" + idtransaksi + "'";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            if (rs.next()) {
                 namapelanggan.setText(rs.getString(2));
                 namamobil.setText(rs.getString(3));
                 tanggalpengambilan.setDate(rs.getDate(4));
                 tanggalpengembalian.setDate(rs.getDate(4));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Edit2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
