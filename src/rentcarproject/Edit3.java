    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:

            if ( "".equals(namapelanggan.getText()) || 
                "".equals(namamobil.getText()) || tanggalpengambilan.getDate() == null ||
                tanggalpengembalian.getDate() == null) {

                JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
            } else {               
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
               
                String tanggalTransaksiStr = sdf.format(new Date());
              
                String tanggalPengambilanStr = sdf.format(tanggalpengambilan.getDate());
                String tanggalPengembalianStr = sdf.format(tanggalpengembalian.getDate());
              
                long lamaHariSewa = hitungLamaSewa();
             
                int totalPembayaran = (int) lamaHariSewa * TARIF_PER_HARI;               

                String SQL = "UPDATE datatransaksi SET "
                    + "namapelanggan='" + namapelanggan.getText() + "', "
                    + "mobil='" + namamobil.getText() + "', "
                    + "tanggaltransaksi='" + tanggalTransaksiStr + "', "
                    + "tanggalpengambilan='" + tanggalPengambilanStr + "', "
                    + "tanggalpengembalian='" + tanggalPengembalianStr + "', "
                    + "lamaharisewa='" + lamaHariSewa + "', "
                    + "totalPembayaran='" + totalPembayaran + "' "
                    + "WHERE idtransaksi='" + idtransaksi.getText() + "'";

                int status = KoneksiDB.execute(SQL);
                if (status == 1) {
                    JOptionPane.showMessageDialog(this, "Data Berhasil Diupdate", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Data Gagal diupdate", "Error", JOptionPane.WARNING_MESSAGE);
                }
            }

    }//GEN-LAST:event_submitActionPerformed

