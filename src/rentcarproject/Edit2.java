    private void loadUserData(String idpelanggan) {
        String SQL = "SELECT * FROM datapelanggan WHERE idpelanggan = '" + idpelanggan + "'";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try {
            if (rs.next()) {
                 namapelanggan.setText(rs.getString(2));
                 alamat.setText(rs.getString(3));
                 nohp.setText(rs.getString(4));
                if ("L".equals(rs.getString(5))) {
                    l.setSelected(true);
                } else {
                    p.setSelected(true);
                }
                 jaminan.setSelectedItem(rs.getString(6));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Edit2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

