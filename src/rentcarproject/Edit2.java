    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:      

        if ("".equals(namapelanggan.getText()) ||
                "".equals(alamat.getText()) || "".equals(nohp.getText()) ||
                    "".equals(jaminan.getSelectedItem())) {
        } else {
            String JK = l.isSelected() ? "L" : "P";
            String SQL = "UPDATE datapelanggan SET "
                        + "namapelanggan='" + namapelanggan.getText() + "', "
                        + "alamat='" + alamat.getText() + "', "
                        + "nohp='" + nohp.getText() + "', "
                        + "jeniskelamin='" + JK + "', "
                        + "jaminan='" + jaminan.getSelectedItem() + "' "
                        + "WHERE idpelanggan='" + idpelanggan.getText() + "'";

            int status = KoneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this, "Data Berhasil Diupdate", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data Gagal Diupdate", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }

    }//GEN-LAST:event_submitActionPerformed

